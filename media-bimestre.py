n1 = float(input('Primeiro bimestre: ')) #notas para a media final
n2 = float(input('Segundo bimestre: '))
n3 = float(input('Terceiro bimestre: '))
n4 = float(input('Quarto bimestre: '))
media = (float(n1) + float(n2) + float(n3) + float(n4)) / 4 #soma todas as notas e divide pela quantidade de bimestre (4)
print('A média é de {}.'.format(media))
